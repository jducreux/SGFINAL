    @Override
    public int print(Graphics graf, PageFormat pagfor, int index) throws PrinterException 
    {
        if (index>0){           
            return NO_SUCH_PAGE;        
        }
        
        Graphics2D hub = (Graphics2D) graf;

        hub.translate(pagfor.getImageableX() + 0.1, pagfor.getImageableY() + 0.1);
        hub.scale(1.0, 1.0);
        Recibo.printAll(graf);
        return PAGE_EXISTS;   
    }

    
